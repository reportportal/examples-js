const expect = require('chai').expect;
const { ReportingApi } = require('@reportportal/agent-js-webdriverio');

describe('testCaseId for suite/test', () => {
  before(() => {
    ReportingApi.setTestCaseId('TestCaseIdForTheSuite');
  });

  it('should have the explicit testCaseId', () => {
    ReportingApi.setDescription('This test case contains explicit test case id');
    ReportingApi.addAttributes([
      {
        key: 'feature',
        value: 'ExplicitTestCaseId',
      },
    ]);
    ReportingApi.setTestCaseId('TestCaseIdForTheTest');
    expect(true).to.be.true;
  });

  it('should have the autogenerate testCaseId', () => {
    ReportingApi.setDescription('This test case contains automatically generated testcase id');
    ReportingApi.addAttributes([
      {
        key: 'feature',
        value: 'AutoGeneratedTestCaseId',
      },
    ]);
    expect(true).to.be.true;
  });
});
